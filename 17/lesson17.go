// Пакет main - основной пакет программы
package main

// Импорт пакета fmt для работы с вводом/выводом
import "fmt"

// Объявление глобальных переменных
// goodsCount - количество товаров, инициализировано значением 13
// numberPair - номер пары, инициализировано значением 14
// classRoom - номер класса, по умолчанию 0 (целочисленный тип)
var (
	goodsCount = 13
	numberPair = 14
	classRoom  int
)

// Объявление констант
// li - константа с плавающей точкой, значение 3.0
// si - константа с плавающей точкой, значение 8.1
// di - константа без явного значения, получит значение от предыдущей константы в группе (8.1)
const (
	li = 3.0
	si = 8.1
	di
)

// Функция main - точка входа в программу
func main() {
	// a - переменная, инициализированная значением 5
	// b - переменная, инициализированная значением 10
	a := 5
	b := 10
	// Вывод строки "lets GO" и значения переменной a
	fmt.Println("lets GO", a)
	// Изменение значения переменной a на 20
	a = 20
	// Вывод строки "say it" и значений переменных b и a
	fmt.Println("say it", b, a)
	// age - переменная типа int, инициализированная значением 30
	var age int = 30
	// Вывод значения переменной age
	fmt.Println(age)
	// ace - переменная, инициализированная значением 50
	ace := 50
	// Вывод значения переменной ace
	fmt.Println(ace)
	// x, y - переменные типа int, инициализированные значениями 1 и 2 соответственно
	var x, y int = 1, 2
	// Вывод значений переменных x и y
	fmt.Println(x, y)
	// z, d - переменные, инициализированные значениями 3 и 4 соответственно
	z, d := 3, 4
	// Вывод значений переменных z и d
	fmt.Println(z, d)
	// Вывод значений глобальных переменных goodsCount, numberPair и classRoom
	fmt.Println(goodsCount, numberPair, classRoom)
	// Параллельное присваивание: age = 31, ace = 88
	age, ace = 31, 88
	// Вывод обновленных значений переменных age и ace
	fmt.Println(age, ace)
	// pi - локальная константа с плавающей точкой, значение 3.14
	const pi = 3.14
	// Вывод значения константы pi
	fmt.Println(pi)
	// Вывод значений констант li, si и di
	fmt.Println(li, si, di)
}
