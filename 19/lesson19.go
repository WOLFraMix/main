package main

import "fmt"

func main() {
	// Десятичное число (обычная система счисления)
	num1 := 67
	fmt.Println(num1)

	// Двоичное число (префикс 0b)
	num2 := 0b01100010
	fmt.Println(num2)

	// Восьмеричное число (начинается с 0)
	num3 := 02345
	fmt.Println(num3)

	// Шестнадцатеричное число (префикс 0x)
	num4 := 0x7bf2
	fmt.Println(num4)
}
