package main

import "fmt"

// main является точкой входа в программу
func main() {
	// Выводит приветственное сообщение на экран
	fmt.Println("Привет, мир!")

	// Вызывает функцию calculateSum для вычисления суммы
	result := calculateSum(5, 3)

	// Выводит результат вычисления на экран
	fmt.Printf("Результат сложения: %d\n", result)
}

// calculateSum вычисляет сумму двух целых чисел и возвращает результат
// a - первое целое число
// b - второе целое число
// возвращает сумму a и b
func calculateSum(a int, b int) int {
	// Выполняет операцию сложения и возвращает результат
	return a + b
}
