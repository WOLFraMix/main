package main

import "fmt"

func main() {
	num1 := 12
	num2 := 0.5
	str := "hello"
	fmt.Printf("Тип переменной num1: %T\n", num1) // Тип переменной num1: int
	fmt.Printf("Тип переменной num2: %T\n", num2) // Тип переменной num2: float64
	fmt.Printf("Тип переменной str: %T\n", str)   // Тип переменной str: string
}

// определение типов переменных
